// Parse raw data and build the output object
// ---------------------------------------------------------------------------

function parseRawData(raw) {
  const colors = {};
  const seen = new Set();
  const lines = raw.split("\n").map((l) => l.trim()).filter(Boolean);

  for (const line of lines) {
    // Format: "PANTONE <name> C,<code>,<L>,<a>,<b>"
    const match = line.match(
      /^PANTONE\s+(.+?)\s+C,\d+,(-?\d+),(-?\d+),(-?\d+)$/
    );
    if (!match) continue;

    const fullName = match[1].trim();
    const L = parseInt(match[2], 10);
    const a = parseInt(match[3], 10);
    const b = parseInt(match[4], 10);

    // Use fullName as key, skip duplicates (keep first occurrence)
    if (seen.has(fullName)) continue;
    seen.add(fullName);

    const hex = labToHex(L, a, b);

    // Determine a clean key: numeric or named
    const key = fullName.replace(/\s+/g, " ");

    colors[key] = {
      name: "PANTONE " + key + " C",
      hex: hex,
      L: L,
      a: a,
      b: b,
    };
  }

  return colors;
}

