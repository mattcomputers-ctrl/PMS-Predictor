/**
 * CIE L*a*b* -> XYZ (D65 illuminant)
 */
function labToXyz(L, a, b) {
  // D65 reference white
  const Xn = 0.95047;
  const Yn = 1.0;
  const Zn = 1.08883;

  const fy = (L + 16) / 116;
  const fx = a / 500 + fy;
  const fz = fy - b / 200;

  const delta = 6 / 29;
  const delta3 = delta * delta * delta;

  const X = Xn * (fx > delta ? fx * fx * fx : (fx - 16 / 116) * 3 * delta * delta);
  const Y = Yn * (fy > delta ? fy * fy * fy : (fy - 16 / 116) * 3 * delta * delta);
  const Z = Zn * (fz > delta ? fz * fz * fz : (fz - 16 / 116) * 3 * delta * delta);

  return [X, Y, Z];
}

/**
 * XYZ -> linear sRGB
 */
function xyzToLinearRgb(X, Y, Z) {
  const r =  3.2404542 * X - 1.5371385 * Y - 0.4985314 * Z;
  const g = -0.9692660 * X + 1.8760108 * Y + 0.0415560 * Z;
  const b =  0.0556434 * X - 0.2040259 * Y + 1.0572252 * Z;
  return [r, g, b];
}

/**
 * Apply sRGB gamma
 */
function linearToSrgb(c) {
  if (c <= 0.0031308) {
    return 12.92 * c;
  }
  return 1.055 * Math.pow(c, 1 / 2.4) - 0.055;
}

/**
 * L*a*b* -> hex string (#RRGGBB)
 */
function labToHex(L, a, b) {
  const [X, Y, Z] = labToXyz(L, a, b);
  const [lr, lg, lb] = xyzToLinearRgb(X, Y, Z);

  const R = Math.round(Math.min(255, Math.max(0, linearToSrgb(lr) * 255)));
  const G = Math.round(Math.min(255, Math.max(0, linearToSrgb(lg) * 255)));
  const B = Math.round(Math.min(255, Math.max(0, linearToSrgb(lb) * 255)));

  return (
    "#" +
    R.toString(16).padStart(2, "0").toUpperCase() +
    G.toString(16).padStart(2, "0").toUpperCase() +
    B.toString(16).padStart(2, "0").toUpperCase()
  );
}

