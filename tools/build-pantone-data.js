function main() {
  console.log("Parsing Pantone data...");
  const colors = parseRawData(RAW_DATA);

  const count = Object.keys(colors).length;
  console.log(`Parsed ${count} unique Pantone Solid Coated colors.`);

  const output = {
    meta: {
      source:
        "Community reference data (unofficial Pantone Solid Coated 2024 v5), Lab values from Pantone specifications",
      illuminant: "D65",
      colorSpace: "CIE L*a*b*",
      hexConversion: "Lab -> XYZ (D65) -> sRGB",
      count: count,
      generated: "2026-04-03",
    },
    colors: colors,
  };

  const outDir = path.join(__dirname, "..", "data");
  if (!fs.existsSync(outDir)) {
    fs.mkdirSync(outDir, { recursive: true });
  }

  const outPath = path.join(outDir, "pantone_lab.json");
  fs.writeFileSync(outPath, JSON.stringify(output, null, 2), "utf-8");
  console.log(`Written to: ${outPath}`);

  // Print a few samples
  const sampleKeys = ["100", "185", "286", "354", "Reflex Blue", "Warm Red 1", "Cool Gray 5", "Black", "White", "Yellow"];
  console.log("\nSample entries:");
  for (const k of sampleKeys) {
    if (colors[k]) {
      const c = colors[k];
      console.log(`  ${c.name}: hex=${c.hex} L=${c.L} a=${c.a} b=${c.b}`);
    }
  }
}

